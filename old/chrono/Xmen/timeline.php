<div class="box b1" data-anim="1">
    <h2>X-men Le Commencement(2011)</h2>
    <p>
    Avant que les mutants n'aient révélé leur existence au monde, et avant que Charles Xavier et Erik Lehnsherr ne deviennent le Professeur X et Magneto, ils n'étaient encore que deux jeunes hommes découvrant leurs pouvoirs pour la premire fois.
    </p>
</div>
<div class="box b2" data-anim="2">
    <h2>X-men Origine Wolverine(2009)</h2>
    <p>
    Depuis la mort de leurs parents, James Logan et Victor Creed veillent l'un sur l'autre. Ils rejoignent l'armée et participent à tous les conflits du XXe siècle. Pendant la guerre du Viêtnam, Victor tue l'un de ses supérieurs et les deux frères sont condamnés à mort. Ils acceptent alors la proposition de William Stryker de rejoindre une unité d'élite composée de mutants.
    </p>
</div>
<div class="box b3" data-anim="3">
    <h2>X-men(2001)</h2>
    <p>
    Séparé par la force de ses parents, le jeune Erik Magnus Lehnsherr se découvre d'étranges pouvoirs sous le coup de la colère : il peut contrôler les métaux. C'est un mutant. Soixante ans plus tard, l'existence des mutants est reconnue mais provoque toujours un vif émoi au sein de la population.
    </p>
</div>
<div class="box b4" data-anim="4">
    <h2>X-men 2(2003)</h2>
    <p>
    Toujours considérés comme des monstres par une société qui les rejette, les mutants sont une nouvelle fois au centre des débats. Un crime effroyable vient d'être commis par l'un deux, qui relance la polémique autour de l'acte d'enregistrement des mutants et le mouvement anti-mutants, dirigé par William Stryker.
    </p>
</div>
<div class="box b5" data-anim="5">
    <h2>X-Men : L'Affrontement final(2006) </h2>
    <p>
    Les mutants doivent faire face à un choix historique : un traitement leur permet désormais d'échapper à leur condition mutante. Pour la première fois, ils ont la possibilité de conserver ce qui fait leur caractère unique mais leur vaut la défiance et la méfiance de l'humanité, ou bien abandonner leurs pouvoirs et devenir des humains à part entière.
    </p>
</div>
<div class="box b6" data-anim="6">
    <h2>Wolverine : Le Combat de l'immortel(2013)</h2>
    <p>
    Logan, l'éternel guerrier marginal, vit reclus dans les montagnes et fait difficilement le deuil de Jean Grey. Sur invitation donnée par Yukio, une mutante capable de lire l'avenir et de voir les morts des autres, il se rend au Japon où Yashida, un ancien officier japonais dont il a sauvé la vie en 1945 lors du bombardement de Nagasaki, lui propose un marché : en échange de sa capacité à guérir, il s'engage à le rendre mortel à nouveau.
    </p>
</div>
<div class="box b7" data-anim="7">
    <h2>X-Men : Days of Future Past(2014)</h2>
    <p>
    Dans un futur proche, humains et mutants, les X-Men, ont été décimés par les sentinelles, des robots capables de s'approprier les pouvoirs des mutants. Seuls quelques X-Men repliés dans un monastère en Chine résistent. Pour tenter de renverser le cours de l'histoire dans un effort ultime, l'avatar de Wolverine est envoyé dans le passé afin de retrouver les professeurs Charles Xavier et Magnéto lorsqu'ils étaient jeunes.
    </p>
</div>
<div class="box b8" data-anim="8">
    <h2>X-Men : Apocalypse(2016)</h2>
    <p>
    Dix années ont passé depuis que le professeur Xavier et les X-Men ont réussi à stopper le déploiement des Sentinelles, mais un nouveau voyage dans le temps modifie le présent et provoque, en Égypte, le réveil d'Apocalypse, le premier mutant de l'Histoire. Immortel, il est l'un des plus puissants. L'humanité et les mutants de la Terre sont une nouvelle fois en danger.
    </p>
</div>
<div class="box b9" data-anim="9">
    <h2>X-Men : Dark Phoenix(2019)</h2>
    <p>
    Au cours d'une mission de sauvetage dans l'espace, Jean Grey frôle la mort, frappée par une mystérieuse force cosmique. De retour sur Terre, cette force la rend non seulement infiniment plus puissante, mais aussi beaucoup plus instable.
    </p>
</div>
<div class="box b10" data-anim="1.">
    <h2>Les Nouveaux Mutants(2020)</h2>
    <p>
    Lorsqu'ils découvrent leurs pouvoirs, c'est à ce moment que les mutants sont les plus dangereux, pour eux-mêmes comme pour les autres. Détenus dans une division secrète contre leur volonté, cinq nouveaux mutants doivent apprivoiser leurs dons et assumer les erreurs graves de leur passé. Traqués par une puissance surnaturelle, leurs peurs les plus terrifiantes vont devenir réalité.
    </p>
</div>
<div class="box b11" data-anim="11">
    <h2>Deadpool(2016)</h2>
    <p>
    Wade Wilson, un ancien militaire des forces spéciales, est devenu mercenaire. Après avoir subi une expérimentation hors-norme qui va accélérer ses pouvoirs de guérison, il va devenir Deadpool. Armé de ses nouvelles capacités et d'un humour noir survolté, il va traquer l'homme qui a bien failli anéantir sa vie.
    </p>
</div>
<div class="box b12" data-anim="12">
    <h2>Deadpool 2(2018)</h2>
    <p>
    L'insolent mercenaire de Marvel est de retour. Dans cet opus, Deadpool devra réunir une équipe de mutants dissidents pour protéger un garçon aux habilités extraordinaires de Cable, un super-soldat cyborg malveillant voyageant dans le temps. Bottage de fesses en perspective, car pour faire le bien, il faut parfois savoir faire le mal..
    </p>
</div>
<div class="box b13" data-anim="13">
    <h2>Logan(2017)</h2>
    <p>
    Dans un futur proche, un certain Logan, épuisé de fatigue, s'occupe d'un Professeur X souffrant, dans un lieu gardé secret à la frontière Mexicaine. Cependant, les tentatives de Logan pour se retrancher du monde et rompre avec son passé vont s'épuiser lorsqu'une jeune mutante traquée par de sombres individus va se retrouver soudainement face à lui.
    </p>
</div>
